import streamlit as st
import pandas as pd

# Создаем тестовые данные для таблиц
data1 = {
    'Имя': ['Алексей', 'Ирина', 'Михаил'],
    'Возраст': [25, 32, 19],
    'Город': ['Москва', 'Санкт-Петербург', 'Казань']
}

data2 = {
    'Продукт': ['Книга', 'Ноутбук', 'Ручка'],
    'Цена': [500, 45000, 20],
    'Количество': [10, 7, 100]
}

# Преобразуем словари в DataFrame
df1 = pd.DataFrame(data1)
df2 = pd.DataFrame(data2)

# Создаем вкладки для переключения между таблицами
tab1, tab2 = st.tabs(["Первая таблица", "Вторая таблица"])

with tab1:
    st.write("Таблица 1")
    st.dataframe(df1)

with tab2:
    st.write("Таблица 2")
    st.dataframe(df2)
